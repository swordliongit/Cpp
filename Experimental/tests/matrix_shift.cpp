void shiftMatrixDownOnce() {
    int numRows = PatternAnimator::grid.size();
    int numCols = PatternAnimator::grid[0].size();

    // Save the last row
    std::vector<int> lastRow = PatternAnimator::grid[numRows - 1];

    // Shift each row down by one position
    for (int i = numRows - 1; i > 0; --i) {
        PatternAnimator::grid[i] = PatternAnimator::grid[i - 1];
    }

    // Place the last row at the top
    PatternAnimator::grid[0] = lastRow;
}

